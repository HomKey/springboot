public class DeviceDefine {
    private String name;
    private String registerIndex;
    private String scale;
    private String divider;
    private String isMean;
    private String desc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegisterIndex() {
        return registerIndex;
    }

    public void setRegisterIndex(String registerIndex) {
        this.registerIndex = registerIndex;
    }

    public String getScale() {
        return scale;
    }

    public void setScale(String scale) {
        this.scale = scale;
    }

    public String getDivider() {
        return divider;
    }

    public void setDivider(String divider) {
        this.divider = divider;
    }

    public String getIsMean() {
        return isMean;
    }

    public void setIsMean(String isMean) {
        this.isMean = isMean;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

}
